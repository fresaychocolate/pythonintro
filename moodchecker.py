mood = input("How are you doing?")

if (mood == "happy"):
    print("It is great to see you happy!")
elif (mood == "nervous"):
    print("Take a deep breath 3 times.")
elif (mood == "sad"):
    print("Don't worry. It'll be okay.")
elif (mood == "excited"):
    print("It is great to see you excited!")
elif (mood == "relaxed"):
    print("It is great to see you happy!")
else:
    print ("I don't recognize this mood")
